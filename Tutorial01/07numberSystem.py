# Print the binary representation of the number 25
print(bin(25))     # Output: '0b11001' (binary representation of 25)

# Print a binary literal directly
print(0b0101)      # Output: 5 (decimal value of binary 0101)

# Print the octal representation of the number 25
print(oct(25))     # Output: '0o31' (octal representation of 25)

# Print the hexadecimal representation of the number 10
print(hex(10))     # Output: '0xa' (hex representation of 10)

# Print a hexadecimal literal directly
print(0xf)         # Output: 15 (decimal value of hex f)
